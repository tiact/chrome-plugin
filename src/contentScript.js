'use strict';

// Content script file will run in the context of web page.
// With content script you can manipulate the web pages using
// Document Object Model (DOM).
// You can also pass information to the parent extension.

// We execute this script by making an entry in manifest.json file
// under `content_scripts` property

// For more information on Content Scripts,
// See https://developer.chrome.com/extensions/content_scripts

// Log `title` of current active web page
const pageTitle = document.head.getElementsByTagName('title')[0].innerHTML;
console.log(
  `Page title is: '${pageTitle}' - evaluated by Chrome extension's 'contentScript.js' file`
);

// Communicate with background file by sending a message
chrome.runtime.sendMessage(
  {
    type: 'GREETINGS',
    payload: {
      message: 'Hello, my name is Con. I am from ContentScript.',
    },
  },
  response => {
    console.log(response.message);
  }
);

// Listen for message
chrome.runtime.onMessage.addListener((request, sender, sendResponse) => {
  if (request.type === 'COUNT') {
    console.log(`Current count is ${request.payload.count}`);
  }

  // Send an empty response
  // See https://github.com/mozilla/webextension-polyfill/issues/130#issuecomment-531531890
  sendResponse({});
  return true;
});

let divNode = document.createElement('div')
divNode.style.width="140px"
divNode.style.backgroundColor="rgb(85 170 153)"
divNode.style.position = "fixed"
divNode.style.left = 0
divNode.style.top = 0
divNode.style.zIndex = 10000
divNode.style.textAlign = "center"
$(divNode).css({backgroundColor:'#3cdf94'});
let text = document.createTextNode("大威天龙")
divNode.appendChild(text)
let body = document.getElementsByTagName("body")[0]
body.appendChild(divNode)